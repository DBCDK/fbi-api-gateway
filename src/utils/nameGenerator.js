export function randomElement(seed, arr) {
  let hash = 0;
  if (seed.length === 0) {
    return hash;
  }
  for (let i = 0; i < seed.length; i++) {
    hash = seed.charCodeAt(i) + ((hash << 5) - hash);
    hash = hash & hash;
  }
  hash = ((hash % arr.length) + arr.length) % arr.length;
  return arr[hash];
}

/*
 * Bettina wanted names
 */
export function generateName(seed) {
  return `${randomElement("first" + seed, FIRST_NAMES)} ${randomElement(
    "middle" + seed,
    FIRST_NAMES
  )} ${randomElement("last" + seed, SURNAMES)}`;
}
